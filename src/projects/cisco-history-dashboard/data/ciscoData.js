// Revenue per employee over time (derived metric)
export const revenuePerEmployee = employeeData
  .filter(e => revenueData.find(r => r.year === e.year))
  .map(e => {
    const rev = revenueData.find(r => r.year === e.year);
    return {
      year: e.year,
      revenuePerEmployee: rev ? Math.round((rev.revenue * 1000000000) / e.employees) : 0,
      employees: e.employees,
      revenue: rev?.revenue || 0,
    };
  });
